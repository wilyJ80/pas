package SOLIDroteiros.roteiro7.parte2;

public final class Component {
	private String nome;

	public Component(String nome) {
		this.nome = nome;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	@Override
	public String toString() {
		return nome;
	}

}
